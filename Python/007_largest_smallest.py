list=["22","33","44","55","66","77","88","99"]
print("The largest number in the list is:",max(list))
print("The smallest number in the list is:",min(list))
#min and max functions are used to find the smallest and largest number in the list


sorted_list=sorted(list)
print("The sorted list is:",sorted_list)
print("max of list is:",sorted_list[-1])
print("min of list is:",sorted_list[0])
#sorted() function returns a new list of sorted elements

